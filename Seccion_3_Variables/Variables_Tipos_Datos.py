#Ejemplo de tipos de datos de Python

#Entero
edad = 30
print('Edad', edad)

#Numero con punto flotante
altura = 1.75
print('Altura', altura)

#Cadena de texto
nombre = 'Josh'
print('Nombre:', nombre)

#Tipo boolean
es_estudiante = False
print('¿Este we, es estudiante?', es_estudiante)

#Tipo None (es ausencia de valor)
direccion = None

print('Dirección :', direccion)
