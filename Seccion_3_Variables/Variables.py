# Variables en Python

# Declaracion e Inicializacion de variables
edad = 30
altura = 1.82
pais = "Mexico"


# Acceder a las variables
print("Valores iniciales:")
print( "Edad:", edad)
print("Altura:", altura)
print("Pais:", pais)

# Modificar el valor de una variable
edad = 40
altura = 2.50
pais = "Colombia"

# Accediendo a las variables modificadas
print("Valores Modificados:")
print( "Edad:", edad)
print("Altura:", altura)
print("Pais:", pais)

# En python el tipo es dinamico
edad = "Treinta"
print("Edad:", edad)
