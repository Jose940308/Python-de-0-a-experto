# Nueva linea: '\n' -» Inserta un salto de línea
# Tabulación '\t' -» Inserta un tabulador horizontal, útil para alinear texto
# Comilla Simple '\ ́' -» Permite incluir comillas simles en  cadena delimitada por comilas simples.
# Comilla doble '\''' -» Permite incluir comillas dobles en una cadela delimitada por comilas dobles
# Barra invertida '\\' -» Permite incluir una diagonal o barra invertida en la cadena

print('Hola \nmundo')
print('\tPython \n \t\tes genial')
print('Juan\' \"Perez')
print('Caracter \\ diagonal invertida')
 
